async function post(title, amountMan, rate, deadline, target, region, agency, status, content, officialUrl, applicationUrl) {
  const r = await fetch(`${BASE}/wp-json/wp/v2/subsidies`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json', Authorization: AUTH },
    body: JSON.stringify({
      title,
      status: 'publish',
      content,
      meta: {
        hj_amount_max: amountMan,       // 万円単位の整数
        hj_amount_rate: rate,
        hj_deadline: deadline,
        hj_target: target,
        hj_region: region,
        hj_agency: agency,
        hj_status: status || '公募中',
        hj_official_url: officialUrl || '',
        hj_application_url: applicationUrl || '',
      }
    }),
  });
  const j = await r.json();
  if (j.id) console.log(`OK [${j.id}]: ${title}`);
  else console.error(`ERR: ${title}`, JSON.stringify(j).slice(0, 120));
  return j;
}

