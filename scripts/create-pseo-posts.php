<h2>ものづくり補助金とは</h2>

ものづくり補助金（正式名称：ものづくり・商業・サービス生産性向上促進補助金）は、<strong>中小企業・小規模事業者が革新的なサービス開発・試作品開発・生産プロセスの改善</strong>を行う際の設備投資等を支援する補助金です。

経済産業省が所管し、全国中小企業団体中央会が実施機関となっています。

[hj_point]
・補助上限額：1,250万円（通常枠）
・補助率：中小企業1/2、小規模事業者2/3
