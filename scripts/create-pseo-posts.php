<h2>事業再構築補助金とは</h2>

事業再構築補助金は、<strong>新分野展開・業態転換・事業転換・業種転換・事業再編などの大胆な事業再構築に取り組む中小企業等を支援する</strong>補助金です。経済産業省が所管し、中小企業庁が運営しています。

<h2>2025年度の申請枠と補助額</h2>

<h3>成長枠</h3>

成長分野への大胆な事業再構築が対象。
・補助上限：7,000万円（中小企業）
・補助率：1/2（中小企業）、1/3（中堅企業）

<h3>コロナ回復加速化枠</h3>

・補助上限：1,500万円
・補助率：2/3〜3/4

<h3>最低賃金枠</h3>

最低賃金引上げの影響を受ける企業向け。
・補助上限：1,500万円
・補助率：3/4〜4/5

<h2>申請要件</h2>

<h3>再構築要件</h3>

以下のいずれかの「再構築」に取り組むことが必要です。

[hj_point]
再構築の類型：
・新分野展開：主たる業種または事業を変えずに新製品・新サービスで新市場に進出
・業態転換：提供方法を大きく変える（例：イートイン→テイクアウト専門）
・事業転換：主な事業を変更する
・業種転換：主な業種を変更する
・事業再編：M&A等による事業統合
[/hj_point]

<h3>認定支援機関との連携</h3>

認定経営革新等支援機関と事業計画を策定し、確認を受けることが必須です。

<h2>採択率の傾向</h2>

事業再構築補助金の採択率は公募回によって変動しますが、概ね30〜50%台で推移しています。他の補助金と比べると採択率は比較的高い水準にあります。

[hj_infobox type="info" title="採択されやすい事業計画のポイント"]
・「再構築」の必要性・必然性を明確に説明できている
